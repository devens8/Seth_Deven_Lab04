public class Lab_04_04_EvenOdd {

    public static void main(String[] args) {
        //variables
        int input = 10;

        //Output
        if (input % 2 == 0){
            System.out.println("The number " + input + " is even");
        } else {
            System.out.println("The number " + input + "is odd");
        }
    }
}
